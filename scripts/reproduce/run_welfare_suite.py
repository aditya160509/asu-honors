class Laptop:
    def __init__(self,brand,ram,price):
        self.brand=brand
        self.ram=ram
        self.price=price

laptop1=Laptop("dell",16,1000)



def getbrand(self):
    return self.brand

def getram(self):
    return self.ram

def getprice(self):
    return self.price

print(laptop1.getbrand())
print(laptop1.getram())
print(laptop1.getprice())
